
public class Operator5 {
	public static void main(String[] args) {
		// 상등연산자: ==, !=
		int num = 3;
		if (num == 7) {
			System.out.println("맞다");
		}

		if (num != 7) {
			System.out.println("다르다");
		}

		// 비교연산자: >, <, >=, <=
		if (num <= 3) {
			System.out.println("맞다");
		}
	}
}
