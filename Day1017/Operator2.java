
public class Operator2 {
public static void main(String[] args) {
	int num1 = 10;
	int num2 = 6;
	
	System.out.println(num1 + num2);
	System.out.println(num1 - num2);
	System.out.println(num1 * num2);
	System.out.println(num1 / num2);
	System.out.println(num1 / (double)num2);
	System.out.println(366 / 4.0);
	System.out.println(366 % 4.0);
}
}
